print("Conhecendo os operadores de comparação")

# IGUALDADE
saldo = 450
saque = 451
print(saldo == saque)

# DIFERENÇA
saldo = 450
saque = 451
print(saldo != saque)

# MAIOR QUE / MAIOR OU IGUAL
saldo = 450
saque = 451
print(saldo > saque)
print(saldo >= saque)

# MENOR QUE / MENOR OU IGUAL
saldo = 450
saque = 451
print(saldo < saque)
print(saldo <= saque)

