print("Conhecendo os operadores de atribuição")

# Atribuição simples:
saldo = 1.00
print(saldo)

# Atribuição com adição
saldo = 1.00
saldo = saldo + 2.00 # ou saldo += 2.00
print(saldo)

# Atribuição com subtração
saldo = 10.00
saldo = saldo - 2.00 # ou saldo -= 2.00
print(saldo)

# Atribuição com multiplição
saldo = 11.00
saldo = saldo * 2.00 # ou saldo *= 2.00
print(saldo)

# Atribuição com divisão
saldo = 12.00
saldo = saldo / 2.00 # ou saldo /= 2.00
print(saldo)

print("%"*20)
