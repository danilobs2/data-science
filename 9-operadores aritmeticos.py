print("Conhecendo os operadores aritméticos")

#Adição
print(1+1)

#Subtração
print(4-1)

#Multiplicação
print(4*4)

#Divisao
print(12/3)

#Divisao inteira
print(12//2)

#Modulo
print(10%3)

#Exponenciação
print(4**4)

print("Precedência de operadores")
'''
Na matemática
A definição indica a seguinte ordem como a correta:

● Parêntesis
● Expoêntes
● Multiplicações e divisões (da esquerda para a direita)
● Somas e subtrações (da esquerda para adireita)

'''

print(10 - 5 * 2)

print((10-5) * 2)

print(10 ** 2 * 2)

print(10 ** (2 * 2))

print(10 / 2 * 4)
