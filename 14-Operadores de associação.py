print("Operadores de associação")

# in = verifica  se o objeto está presente em uma sequência
# not in  = verifica  se o objeto não está presente em uma sequência
'''
São operadores utilizados para verificar se um objeto está presente em uma sequência
'''

curso = "Curso de Python"
frutas = ["laranja", "uva", "limão"]
saques = [1500, 100]

var = "Python" in curso
print(var)

print("maça" not in frutas) # maça não está presente na lista fruta

print(200 in saques)
